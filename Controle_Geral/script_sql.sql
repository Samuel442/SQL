-- Criação projeto
CREATE DATABASE CONTROLE_GERAL

USE CONTROLE_GERAL

-- Criação da tabela
CREATE TABLE ESTOQUE
(
	ID                INT IDENTITY(1,2),
	QUANTIDADE        INT
	, NOME_PRD        VARCHAR(255)
	, DISPONIVEL      VARCHAR(255)
	, NUMERO_DA_NOTA  INT
)

-- Criação da tabela
CREATE TABLE VENDA
(
    ID           INT IDENTITY(1,1),
	VALOR        FLOAT 
	, PROD       VARCHAR(255)
	, QUANTIDADE INT
	, VENDEDOR   VARCHAR(255)
	, FILIAL     VARCHAR(255)
)


-- Criação da tabela
CREATE TABLE CLIENTES
(
    ID         INT IDENTITY(1,1),
	NOME       VARCHAR(255)
	, CPF      VARCHAR(255)
	, ENDE     VARCHAR(255)
	, SEXO     VARCHAR(255)
	, CIDADE   VARCHAR(255)
)

